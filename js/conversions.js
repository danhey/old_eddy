function yearsToDays(year) {
    return (year*365);
}

function daysToYears(day) {
    return (day*0.0027397260273973);
}

function degreeToRad(degree) {
    return (degree*(Math.PI/180));
}

function radToDegree(rad) {
    return (rad * (Math.PI/180));
}

function lsToAu(ls) {
    return (ls*0.002003988804221111);
}

function auToLs(au) {
    return (au * 499.004783806);
}
